# CREO EL ARREGLO DE PROFESORES
profesores = ["Monasterio","Carrizo","Noli","Palacios","Brizuela"]
# INICIO LA VARIABLE POSICION EN CERO
posicion = 0
# PARA CADA PERSONA DENTRO DEL ARREGLO PROFESORES
for persona in profesores:
  # A LA VARIABLE POSICION LE SUMO 1
  posicion = posicion+1
  print("Posicion ", posicion, " Profesor ", persona)